    public AIJFrame(int depth,int type,User user) {
        this.user=user;
        initComponents();
        player=new AudioFilePlayer("src/musics/磯村由紀子 (矶村由纪子) - 风居住的街道.mp3");
        board=new AIBoard(depth, type,user);
        buttonGroup.add(lowItem);
        buttonGroup.add(primaryItem);
        buttonGroup.add(midItem);
        buttonGroup.add(highItem);
        setDepth();
        if(depth==1){
            lowItem.setSelected(true);
        }else if(depth==2){
            primaryItem.setSelected(true);
        }else if(depth==3){
            midItem.setSelected(true);
        }else{
            highItem.setSelected(true);
        }
        if(type==ComputerPlayer.MAX)
            AITypeItem.setSelected(true);
        else if(type==ComputerPlayer.MIN)
            AITypeItem.setSelected(false);
        getContentPane().add(board);
        setResizable(false);
        player.start();
    }
    private void setDepth(){
       if(user.getScore()<300){
           primaryItem.setEnabled(false);
           midItem.setEnabled(false);
           highItem.setEnabled(false);
       }else if(user.getScore()<700){
           midItem.setEnabled(false);
           highItem.setEnabled(false);
       }else if(user.getScore()<1200){
           highItem.setEnabled(false);
       }
    }
