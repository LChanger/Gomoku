    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        UserManager um=new UserManager();
        User temp;
        if((temp=um.findUser(user.getLogId()))!=null)
            user=temp;
        setDepth();
        board.restart();
        board.resetIsEnd();
        if(lowItem.isSelected())
            board.setDepth(1);
        if(primaryItem.isSelected())
            board.setDepth(2);
        if(midItem.isSelected())
            board.setDepth(3);
        if(highItem.isSelected())
            board.setDepth(4);
        if(AITypeItem.isSelected()){
            board.setAIColor(ComputerPlayer.MAX);
            board.AIFirst();
        }
        else{
            board.setAIColor(ComputerPlayer.MIN);      
        }

    }//GEN-LAST:event_startButtonActionPerformed

